print("Calculadora")

print("Ingresar el primer numero")
num1 = int(input())
print("Ingresar el segundo numero")
num2 = int(input())

print("Elegir operacion")
print("1 - suma")
print("2 - resta")
print("3 - multiplicacion")
print("4 - division")
operacion = int(input())

if operacion == 1:
    print("La suma es: ", num1 + num2)
if operacion == 2:
    print("La suma es: ", num1 - num2)
if operacion == 3:
    print("La suma es: ", num1 * num2)
if operacion == 4:
    print("La suma es: ", num1 / num2)
